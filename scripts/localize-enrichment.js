const generatedBlockPattern =
  /<section class="evidence-section" data-professional-enrichment[\s\S]*?<section class="cta-band"[\s\S]*?<\/section>/g;

function localizeText(text) {
  return replacements.reduce((value, [pattern, replacement]) => value.replace(pattern, replacement), text);
}

function localizeHtmlText(block) {
  return block.replace(/(<[^>]+>)|([^<]+)/g, (match, tag, text) => (tag ? tag : localizeText(text)));
}

let changed = 0;
for (const file of files) {
  const html = fs.readFileSync(file, "utf8");
  const next = html.replace(generatedBlockPattern, (block) => localizeHtmlText(block));
  if (next !== html) {
    fs.writeFileSync(file, next, "utf8");
    changed += 1;
  }
}

console.log(`Localized ${changed} enriched pages`);
